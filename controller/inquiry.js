const Inquiry = require("../models/inquiry");

exports.addinquiry = async (req, res) => {
  const { name, product, email, desc, mobile } = req.body;

  const newInquiry = new Inquiry({
    name: name,
    product: product,
    email: email,
    desc: desc,
    mobile: mobile,
  });

  newInquiry
    .save()
    .then((result) => {
      res.status(200).json({
        status: true,
        msg: "success",
        data: result,
      });
    })
    .catch((error) => {
      res.status(400).json({
        status: false,
        msg: "error",
        error: error,
      });
    });
};

exports.editinquiry = async (req, res) => {
  const findandUpdateEntry = await Inquiry.findOneAndUpdate(
    {
      _id: req.params.id,
    },
    { $set: req.body },
    { new: true }
  );
  if (findandUpdateEntry) {
    res.status(200).json({
      status: true,
      msg: "success",
      data: findandUpdateEntry,
    });
  } else {
    res.status(400).json({
      status: false,
      msg: "error",
      error: "error",
    });
  }
};

